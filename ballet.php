  <div id="Echappe">
    <h2><strong>&Eacute;chapp&eacute;</strong></h2>
    <p>&nbsp;</p>
    <p>Deslizar  el movimiento. Un &eacute;chapp&eacute; es una abertura de ambos pies de una posici&oacute;n cerrada  a una posici&oacute;n abierta. Hay dos clases de &eacute;chapp&eacute;s: &eacute;chapp&eacute; saut&eacute;, que es  saltado, y echepp&eacute; sur les pointes &nbsp;o demi-pointes, que se hace con un  relev&eacute; y tiene las rodillas rectas cuando la posici&oacute;n es abierta. En cada caso  los &eacute;chapp&eacute;s se hacen a la segunda o cuarta posici&oacute;n, ambos pies que viajan una  distancia igual del centro de gravedad original.<br>
    </p>
  </div>
  <div id="Developpe">
    <h2><strong>D&eacute;velopp&eacute;, Temps</strong></h2>
    <p>&nbsp;</p>
    <p>El  tiempo de desarroll&oacute; de un movimiento. Por el uso com&uacute;n el t&eacute;rmino se ha  abreviado a d&eacute;velopp&eacute;. Un d&eacute;velopp&eacute; es un movimiento en el que la pierna que  trabaja es elevada al nivel de la rodilla de la pierna de apoyo y despacio  ampliada a una posici&oacute;n abierta en l'air y sostenida all&iacute; con un control  perfecto. Las caderas son mantenidas a nivel y en la direcci&oacute;n en la que el  bailar&iacute;n se enfrenta.</p>
    <p>&nbsp;</p>
