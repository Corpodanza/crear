  </div>
  <div id="balance">
    <h2><strong>Balanc&eacute;</strong></h2>
    <p> <br>
      Paso oscilante. Este paso es muy parecido al  movimiento del valse y es una alternaci&oacute;n de equilibrio, cambiando el peso de  un pie al otro.</p>
  </div>
  <div id="Attitude" onfocus="MM_showHideLayers('Arabesque','','hide','Assemble','','hide','posicion','','hide','Attitude','','show','adagio','','hide','allegro','','hide')">
    <h2>Attitude</h2>
    <p>&nbsp;</p>
    <p>Es una posici&oacute;n en una pierna (como arabeque) con  la diferencia que la que se encuentra levantada flexiona la rodilla dobl&aacute;ndola  en un &aacute;ngulo de 90 grados. El pie de soporte puede ser terre, demi-pointe o  pointe. Los brazos se encuentran sostenidos en las varias posiciones que crean  la l&iacute;nea m&aacute;s larga y estilizada. Los hombros se deben sostener cuadrados a la  l&iacute;nea de la direcci&oacute;n.</p>
  </div>
  <p>&nbsp;</p>
    <div id="adagio">
      <h2>Adagio</h2>
      <p>&nbsp;</p>
      <p>Movimiento lento que se realiza solo o acompa&ntilde;ado.</p>
      <p>La  primac&iacute;a cl&aacute;sica del pas de deux, en el cual la bailarina es ayudada por su  compa&ntilde;ero masculino y as&iacute; realiza los movimientos lentos y elevaciones, en el  cual en los ascensos el bailar&iacute;n apoya o lleva a la bailarina. La bailarina  as&iacute;, realiza pasos y alcanza las posturas que le ser&iacute;an imposibles sin la ayuda  de su compa&ntilde;ero.<br>
      </p>
    </div>
    <div id="allegro">
      <h2>All&eacute;gro</h2>
      <p>&nbsp;</p>
      <p>En&eacute;rgico, animado. Un t&eacute;rmino que se aplica a todos  los movimientos brillantes y en&eacute;rgicos. Todos los pasos de saltos tales como el  entrechat, cabriole, assembl&eacute;, jet&eacute; etc. se realizan bajo esta clasificaci&oacute;n.</p>
