   <h2>Historia del Ballet</h2>
   <p><br>
     El ballet naci&oacute; a fines del siglo XV, en los festines de las cortes italianas. Estos fueron ampliamente desarrollados en Francia. El primer baile en Francia, en el a&ntilde;o 1581, denominado &quot;El ballet c&oacute;mico de la reina&quot;. El baile se profundiz&oacute; en el siglo XVII, en las diferentes cortes europeas, con vestuarios, iluminaci&oacute;n, decoraci&oacute;n, etc.</p>
   <p>El ballet alcanz&oacute; su cumbre en el reinado de Luis XIV. En 1661 el rey Luis XIV, fund&oacute; la &quot;Academia Real de Danza&quot;, para maestros. En ese momento, la danza estaba empezando a configurarse como una disciplina profesional. </p>
   <p>Al comienzo, solo los hombres pod&iacute;an participar, y en el caso de los papeles femeninos, se disfrazaban. En el a&ntilde;o 1681, aparecieron bailarinas en &quot;El triunfo del Amor&quot;.</p>
  <p>En europa durante el siglo XIX, el ballet empezaba a perder inter&eacute;s. Mientras en Rusia, sigui&oacute; siendo un exito, gracias al core&oacute;grafo Marius Petipa. Las obras m&aacute;s conocidas de Petipa eran: La bella durmiente, Cascanueces y El Lago de los cisnes, sobre m&uacute;sica de Piotr Ilich Tchaikovski.</p>
   <p>En la actualidad, existen varios tipos de danzas, aunque el principal siempre es el ballet.</p>
