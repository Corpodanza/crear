  <p><strong>Historia de las puntas </strong></p>
   <p>&nbsp;</p>
   <p>Antes, no exist&iacute;a las zapatillas de puntas.</p>
   <p>Lo m&aacute;s importante del ballet, fue la creaci&oacute;n de las zapatillas de punta, usadas solamente por las mujeres. Que al comienzo fue dif&iacute;cil para las bailarinas, ya que reci&eacute;n se hab&iacute;an inventado. Despues, las t&eacute;cnicas fueron avanzando y las zapatillas de puntas fueron un elemento com&uacute;n en el ballet.</p>
   <p>Las zapatillas de punta se usan comunmente a los 9 o 12 a&ntilde;os, cuando los huesos y los m&uacute;sculos estan, generalmente preparados para estas zapatillas. Aunque, hay muchas academias que ense&ntilde;an con las zapatillas de puntas desde los 4 o 6 a&ntilde;os!, como la academia en Londres, Royal Ballet. </p>
