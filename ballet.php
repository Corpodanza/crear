    <h2><strong>Ron de Jambe</strong> </h2>
    <p><br>
      Movimiento redondo de la pierna, es decir, un  movimiento circular de la pierna sin mover las caderas y la pelvis. El ronds de  jambe se utiliza como ejercicio en la barra, en el centro y puede hacerse a  terre o en l'air. Todos los que se hacen hacia la derecha son d&eacute;hors y a la  izquierda los dedans.</p>
