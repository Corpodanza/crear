  <div id="Releve">
    <h2><strong>Relev&eacute;</strong> </h2>
    <p><br>
      Levantado. Es subir a la media punta o punta. Hay  dos maneras de relev&eacute;. En&nbsp; la escuela francesa, el relev&eacute; se hace con una  subida lisa, continua mientras que el m&eacute;todo de Cecchetti y la escuela rusa  utilizan un peque&ntilde;o resorte. Relev&eacute; se puede hacer en las cinco posiciones,  arabesco devant, derri&egrave;re, en tournant, en del pass&eacute; avant, arri&egrave;re del en del  pass&eacute; etc.</p>
