    <h2><strong>Battement Frapp&eacute;</strong></h2>
    <p>&nbsp;</p>
    <p>Battement  pulsado. Un ejercicio en el cual el bailar&iacute;n poderosamente ampl&iacute;a la pierna de  trabajo&nbsp; de una posici&oacute;n cou de pie a varias posiciones: al frente, al  lado o a la parte posterior. Este ejercicio consolida los dedos del pie y los  empeines y desarrolla la energ&iacute;a de las puntas. Es la base del allegro.<br>
    </p>
  </div>
  <div id="Fondu">
    <h2><strong>Battement Fond&uacute;</strong> <br>
    </h2>
    <p>&nbsp;</p>
    <p>Es un ejercicio en el cual la pierna soporte est&aacute;  doblada lentamente en demi plie (como hundi&eacute;ndose abajo) y el pie que trabaja  se&ntilde;ala el tobillo y se estira hacia el suelo o hacia el aire. Mientras que se  endereza la pierna soporte se extiende. El fondu puede ejecutarse con punta en  el suelo o en el aire. Se puede ejecutar en varias posiciones: al frente, al  lado o atr&aacute;s.</p>
  </div>
  <div id="Degage">
    <h2><strong>Battement D&eacute;gag&eacute;</strong> <br>
    </h2>
    <p>&nbsp;</p>
    <p>El battement d&eacute;gag&eacute; es similar al battement tendu  pero se hace mas r&aacute;pido y el pie de trabajo se levanta cerca de cuatro pulgadas  del suelo con el pie bien acentuado. Al cerrar resbala nuevamente hacia dentro  de la&nbsp; primera o quinta posici&oacute;n. Los d&eacute;gag&eacute;s consolidan los dedos del  pie, desarrollan el empeine y mejoran la flexibilidad del empalme del  tobillo.&nbsp;<br>
      &nbsp;</p>
  </div>
  <div id="Battement">
    <h2><strong>Battement</strong></h2>
    <p> <br>
      Acci&oacute;n que consiste en sacar la pierna extendida.  Hay dos tipos de battements: grands battmente y petites battmente. Los  battements petis son: tendus, d&eacute;gag&eacute;s, frapp&eacute;s. Los gran battement: en cloche,  y estirado.<br>
