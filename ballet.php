          <h2><em><strong>Posiciones de pies&nbsp;</strong> </em></h2>
          <p><em><br>
            En el Ballet hay cinco posiciones b&aacute;sicas de los  pies y cada paso o movimiento comienza y termina en una u otra de estas  posiciones, que fueron establecidas por Pierre Beauchamp, ma&icirc;tre de ballet del  Academia R&oacute;yale de Musique et de Dance a partir de 1671 a 1687.Estas son:</em></p>
          <ul type="disc">
            <li>
              <p><em>Primera       posici&oacute;n (posici&oacute;n de Premi&egrave;re): En esta posici&oacute;n los pies forman una       l&iacute;nea, los talones se tocan uno otro</em></p>
            </li>
          </ul>
          <p><em>.<a href="http://mundoballet.galeon.com/imagenes/foto1.jpg" target="_blank"><img border="0" width="174" height="150" src="images/varios/primera.jpg" alt="Descripci&oacute;n: http://mundoballet.galeon.com/imagenes/foto1.jpg"></a></em></p>
          <ul type="disc">
            <li>
              <p><em>Segunda       posici&oacute;n (posici&oacute;n de Seconde): Los pies est&aacute;n en la misma l&iacute;nea pero con       una distancia de cerca de un pie entre los talones</em></p>
            </li>
          </ul>
          <p><em><img border="0" width="154" height="150" src="images/varios/segunda.jpg" alt="Descripci&oacute;n: http://mundoballet.galeon.com/imagenes/foto3.jpg"></em></p>
          <ul type="disc">
            <li>
              <p><em>Tercera       posici&oacute;n (posici&oacute;n de Troisi&egrave;me): En la tercera posici&oacute;n un pie est&aacute;       delante del otro, se cierra uno delatnet y en el centro del otro pie.</em></p>
            </li>
          </ul>
          <p><em><a href="http://mundoballet.galeon.com/imagenes/foto4.jpg" target="_blank"><img border="0" width="152" height="150" src="images/varios/tercera.jpg" alt="Descripci&oacute;n: http://mundoballet.galeon.com/imagenes/foto4.jpg"></a></em></p>
          <ul type="disc">
            <li>
              <p><em>Cuarta       posici&oacute;n (posici&oacute;n de Quatri&egrave;me): En la cuarta posici&oacute;n la colocaci&oacute;n de       los pies es similar a la tercera posici&oacute;n, los pies son paralelos y       separados por la longitud de un pie y el peso del cuerpo se haya en el       medio de las dos piernas.</em></p>
            </li>
          </ul>
          <p><em><img border="0" width="132" height="150" src="images/varios/cuarta.jpg" alt="Descripci&oacute;n: http://mundoballet.galeon.com/imagenes/foto5.jpg"><a href="http://mundoballet.galeon.com/imagenes/foto5.jpg" target="_blank">i</a></em></p>
          <ul type="disc">
            <li>
              <p><em>Quinta       posici&oacute;n (posici&oacute;n de Cinqui&egrave;me): En la quinto posici&oacute;n, en el m&eacute;todo de       Cecchetti, se cruzan los pies de modo que el primer empalme del dedo gordo       demuestre m&aacute;s all&aacute; de cualquier tal&oacute;n. En las escuelas francesas y del       ruso los pies se cruzan totalmente de modo que el tal&oacute;n del pie delantero       toque el dedo del pie del pie trasero y viceversa.</em></p>
            </li>
          </ul>
          <p><em><img border="0" width="103" height="150" src="images/varios/quinta.jpg" alt="Descripci&oacute;n: http://mundoballet.galeon.com/imagenes/foto7.jpg"> <br>
            <strong>Posiciones de brazos</strong></em></p>
          <ul type="disc">
            <li>
              <p><em><strong>1&ordf;       Posici&oacute;n:</strong>&nbsp;Coloca       a los brazos al frente en una forma oval, con las manos curvadas y los       dedos agrupados con elegancia y naturalidad. El dedo medio se curvan a un       poco m&aacute;s que los otros. Relaja los pulgares para que no se levanten.</em></p>
            </li>
            <li>
              <p><em><strong>2&ordf;       Posici&oacute;n:</strong>&nbsp;Abre       los brazos de forma amplia pero mant&eacute;nlos en l&iacute;nea con los hombros,       ligeramente curvados, con cuidado de no dejar caer los codos. Los dedos       han de aparecer delicados y suaves.</em></p>
            </li>
            <li>
              <p><em><strong>3&ordf;       Posici&oacute;n:</strong>&nbsp;Ante       en brazo curvado delante de ti, ligeramente adelantado respecto a la       cabeza. El otro, extendido y ligeramente curvado, para ayudarte, imagina       que algo te sostiene los dedos y el codo.</em></p>
            </li>
            <li>
              <p><em><strong>4&ordm;       Posici&oacute;n:</strong>&nbsp;Levanta       un brazo un formando una curva graciosa ligeramente por delante de la       cabeza. El otro, extendido y un poco curvado. En todas estas posiciones       conserva encogidos los m&uacute;sculos del est&oacute;mago y del trasero, la espalda       recta y los hombros horizontales y relajados.</em></p>
            </li>
            <li>
              <p><em><strong>5&ordf;       Posici&oacute;n:</strong>&nbsp;Levanta       ambos brazos en una forma oval, enmarcando la cara, pero sin mover los       hombros. Sin alzar la vista, debes saber que tienes los dedos ligeramente       por delante de la cabeza.</em></p>
            </li>
          </ul>
