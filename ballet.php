    <p><br>
      Ejercicio caminando directamente en el punto o el  demi-pointe del pie de trabajo en cualquier direcci&oacute;n o posici&oacute;n deseada y con  el otro pie levant&oacute; en el aire. Por ejemplo, en pique arabesco, pique devel&oacute; pe  etc.</p>
