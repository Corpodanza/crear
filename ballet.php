  <div id="Dedans">
    <h2><strong>Dedans, en</strong></h2>
    <p>&nbsp;</p>
    <p>Hacia  dentro. En pasos y ejercicios el t&eacute;rmino dedans indica que la pierna, en una  posici&oacute;n &agrave; terre o en l'air, se mueve en una direcci&oacute;n circular, en sentido  contrario a las agujas del reloj. Como, por ejemplo, en rond de jambe &agrave; terre  en dedans. En piruetas, el t&eacute;rmino indica que una pirueta es hecha hacia  adentro, hacia la pierna de apoyo.&nbsp;<br>
    </p>
