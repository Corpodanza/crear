    <h2><strong>Glissade</strong></h2>
    <p>&nbsp;</p>
    <p>Deslizamiento. Deslizando el pie de trabajo de la  quinto posici&oacute;n en la direcci&oacute;n requerida, el otro pie se cierra a ella.  Glissade es un paso de suelo y se utiliza principalmente para ligar hacia otros  pasos.&nbsp; El Glissade se puede ejecutar con o sin cambio de pies, y todo  comienza y termina con un demi-pli&eacute;. Hay seis glissades: devant, derri&egrave;re,  dessous, dessus, en avant, arri&egrave;re. La diferencia entre ellos depende &nbsp;de  las posiciones que comienzan y que acaban as&iacute; como la direcci&oacute;n del cuerpo.<br>
