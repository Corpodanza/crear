    <h2><strong>Demi-pli&eacute;</strong></h2>
    <p>&nbsp;</p>
    <p>Mitad flexi&oacute;n de las rodillas. Todos los pasos de  la elevaci&oacute;n comienzan y terminan con un demi-pli&eacute;.</p>
