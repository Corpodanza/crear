  <div id="Dehors">
    <h2><strong>Dehors, en</strong></h2>
    <p>&nbsp;</p>
    <p>Externo. En pasos y ejercicios el t&eacute;rmino en dehors  indica que la pierna, en una posici&oacute;n &agrave; terre o en l'air, se mueve en una  direcci&oacute;n circular, en el sentido de las agujas del reloj. Como, por ejemplo,  en rond de jambe &agrave; terre en dehors. En piruetas el t&eacute;rmino indica que una  pirueta es hecha externa hacia la pierna que trabaja.</p>
