    <h2>Danza Contempor&aacute;nea </h2>
    <p>&nbsp;</p>
    <p>&nbsp;</p>
    <p>-La danza contempor&aacute;nea se desarrolla a finales del  Siglo XIX. Los precursores de la danza moderna son de origen alem&aacute;n y norteamericano, se  puede citar por ejemplo a Lo&iuml;e Fuller e Isadora Duncan. </p>
    <p>-Libre, l&uacute;dica y  creativa, exige tambi&eacute;n una t&eacute;cnica que se define por el reconocimiento y el control de las leyes hunmano-naturales como: el espacio, el tiempo, la energ&iacute;a, la  gravedad. </p>
    <p>-La danza contempor&aacute;nea tiene como meta principal el desarrollo optimo de la expresi&oacute;n del cuerpo y se reconoce como una corriente libre de la danza. </p>
  <p>-En  nuestra Escuela no s&oacute;lo aprender&aacute;s los principios de la danza Contempor&aacute;nea sino  que los estudiantes disfrutar&aacute;n experimentando como se mueve armoniosamente el cuerpo.</p>
