    <h6>COSECHAS</h6>
    <p>&nbsp;</p>
    <p>Es una danza quichua de la provincia de Imbabura. Se realiza  durante los meses de agosto y septiembre en honor a la pachamama (la madre  tierra) y al inti. Esta danza escenifica la labor del campo desde la  preparaci&oacute;n de la tierra trabajada con la fuerza de los hombres, la siembra  realizada por las mujeres, la cosecha del ma&iacute;z o del trigo elaborada por  familiares, vecinos y amigos y finalmente el festejo en el que se acostumbra a  beber aguardiente y la tradicional chicha hasta altas horas de la tarde.</p>
  <p>&nbsp;</p>
    <h6>PINDULLEROS</h6>
    <p><br>
      &nbsp;<br>
      Danza perteneciente a la comunidad de Tizaleo, provincia de  Tungurahua; baile que identifica y exalta la expresi&oacute;n de los campesinos,  algarab&iacute;a ante el encuentro de toda la comuna, reuni&oacute;n de los ayllus (familias)  en las plazas o capillas, fiesta religiosa y bendici&oacute;n de la comuna.</p>
    <p>&nbsp;</p>
    <h6>ZULETA</h6>
    <p>&nbsp;</p>
    <p>Danza de polleras multicolores, de una comunidad de la  Esperanza &ldquo;Zuleta&rdquo;, en la provincia de Imbabura, en la que los hombres y las  mujeres celebran las fiestas religiosas y sociales de la Comunidad luciendo sus  vestimentas y atuendos.</p>
  <p>&nbsp;</p>
    <h6>SAN JUANITO</h6>
    <p>&nbsp;</p>
    <p>Danza, presencia y elegancia, fuerza de una identidad india.  Su danza, sus movimientos expresan la presencia del hombre y la mujer quichua  de igual gallard&iacute;a, manifiestan la relaci&oacute;n que existe entre espacio, tiempo,  cielo y tierra &rdquo; la resistencia de una vivencia llena de energ&iacute;a y valor para  continuar.</p>
  <p>&nbsp;</p>
    <h6>&Ntilde;USTAS</h6>
    <p><br>
      &nbsp;<br>
      Danza interpretada por hermosas mujeres. Sus movimientos  tiernos y dulces que destacan la belleza natural de la mujer, en alabanza de  los dones concebidos por la madre naturaleza procreadora. Adem&aacute;s festejan la  despedida de soltera de una de sus compa&ntilde;eras que contraer&aacute; matrimonio.<br>
      Sus atuendos blancos representan la pureza de la doncella  que llega virgen al matrimonio.</p>
    <p>&nbsp;</p>
    <h6>PENDONEROS</h6>
    <p>&nbsp;</p>
    <p>Se lleva cabo en San Miguel de Cuchillona y Tocag&oacute;n. En  donde nuestros ind&iacute;genas siguiendo las costumbres de sus antepasados despu&eacute;s de  dar muerte a sus enemigos, los suspend&iacute;an en largos palos de pend&oacute;n, sirviendo  de escarmiento para los dem&aacute;s. Es por ello que las banderas rojas simbolizan y  recuerdan la sangre de esas manifestaciones.</p>
  <p>&nbsp;</p>
    <h6>ARUCHICOS y FIESTAS DE SAN PEDRO</h6>
    <p>&nbsp;</p>
    <p>Se celebra a partir del 29 de Junio y sus danzantes visten  zamarros y llevan a sus espaldas unos peque&ntilde;os cueros a los cuales van  amarradas campanas de bronce y cobre, que al bailar con su zapateo hacen sonar  para ahuyentar a los malos esp&iacute;ritus.<br>
  Las mujeres denominadas tambi&eacute;n &ldquo;Chinucas&rdquo; acompa&ntilde;an  bailando y cantando coplas al son de la guitarra.</p>
