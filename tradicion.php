    <h2>BAILES TRADICIONALES DEL ECUADOR<br>
  &nbsp;</h2>
    <p><br>
      En Ecuador se difunde una amplia variedad de g&eacute;neros  musicales,.. Son originarios de este pa&iacute;s ritmos tradicionales como son el San  Juan, danzante, yarav&iacute;, yumbo, fox incaico y ritmos mestizos como el San Juanito,  el pasillo, pasacalle, &nbsp;albazo, tonada,  el alza, capishca, bomba del chota, y ritmos menos conocidos como el amorfino, &nbsp;aire t&iacute;pico. La mayor&iacute;a de estos g&eacute;neros  musicales fueron desarrollados en la sierra ecuatoriana, los mismos que son  variaciones de ritmos cl&aacute;sicos europeos como el pasillo lo es del vals,  incluyendo en ellos gran influencia de la m&uacute;sica ind&iacute;gena andina. </p>
  <p>&nbsp;</p>
